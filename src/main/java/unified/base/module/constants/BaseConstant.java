public class BaseConstant {

    /**
     * 是否删除(0:未删除，1:已删除)
     */
    public static class Deleted {
        /** 0:未删除 */
        public static final int NO = 0;
        /** 1:已删除 */
        public static final int YES = 1;
    }

    /**
     * 是否启用(0:禁用,1:启用)
     */
    public static class Enabled {
        /** 0:禁用 */
        public static final int NO = 0;
        /** 1:启用 */
        public static final int YES = 1;
    }
}
